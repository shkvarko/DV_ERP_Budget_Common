                System.Data.SqlClient.SqlDataReader rs = cmd.ExecuteReader();
                if( rs.HasRows )
                {
                    while( rs.Read() )
                    {
                        objList.Add( new CMeasure( rs.GetGuid( 0 ), rs.GetString( 1 ) ) );
